<h2>一、什么是赛鸽分速？</h2>

<p>赛鸽分速，顾名思义就是鸽子在比赛中的<strong>每分钟飞行距离</strong>，单位是米/分钟（m/min）。它是衡量鸽子飞行速度的最直接指标。一场赛鸽比赛的排名，通常就是按照分速从高到低排列。分速越高，鸽子归巢越快，排名越靠前。</p>

<h2>二、分速计算公式</h2>

<p>分速的计算公式非常简单：</p>

<blockquote><p><strong>分速（m/min）= 空距（米）÷ 飞行时间（分钟）</strong></p></blockquote>

<p>其中：</p>
<ul>
<li><strong>空距</strong>：放飞点到鸽舍的直线距离（由GPS或地图测量确定，单位：米）</li>
<li><strong>飞行时间</strong>：从开笼放飞到鸽子归巢之间的总时长（单位：分钟）</li>
</ul>

<p>举个例子：某场比赛空距为500公里（500,000米），冠军鸽耗时7小时30分钟（450分钟）归巢，则：分速 = 500,000 ÷ 450 ≈ <strong>1,111 米/分钟</strong>。</p>

